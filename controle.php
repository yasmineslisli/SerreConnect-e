    const humidityDisplay = document.querySelector("#humidity-display");
    const humidityHandle = document.querySelector("#humidity-handle");
    const dialCenterHum = document.querySelector("#humidity-dial-center");
  
    const minHumidity = 0;
    const maxHumidity = 100;
    const humidityRange = maxHumidity - minHumidity;
  
    let originHum = calculateRotationOriginHum();
    let rotatingHum = false;
  
    function calculateRotationOriginHum() {
  const { width, height, left, top } = dialCenterHum.getBoundingClientRect();
  return {
    x: left + width / 2,
    y: top + height / 2
  };
}

  
    function handleMouseDownHum(event) {
      rotatingHum = true;
    }
  
    function handleMouseMoveHum(event) {
      if (rotatingHum) {
        const { clientX, clientY } = event;
        rotateHum(clientX, clientY);
      }
    }
  
    function handleTouchMoveHum(event) {
      if (rotatingHum) {
        event.preventDefault();
        const {
          touches: {
            0: { clientX, clientY }
          }
        } = event;
        rotateHum(clientX, clientY);
      }
    }
  
    function handleMouseUpHum(event) {
      rotatingHum = false;
    }
  
    function rotateHum(x, y) {
      const angle = Math.atan2(y - originHum.y, x - originHum.x);
      const angleDegrees = 180 + (angle * 180) / Math.PI;
      updateDialHum(angleDegrees);
    }
  
    function updateDialHum(angle) {
      const percentageOfFullRange = ((360 + (angle - 90)) % 360) / 360;
      const newHumidity = (minHumidity + humidityRange * percentageOfFullRange).toFixed(0);
  
      humidityDisplay.innerHTML = newHumidity;
      document.documentElement.style.setProperty("--humidity-rotation", `${angle}deg`);
    }
  
    humidityHandle.addEventListener("mousedown", handleMouseDownHum);
